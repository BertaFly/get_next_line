int		ft_factorial(int nb)
{
	int res;
	int i;

	if (nb >= 0)
	{
		res = 1;
		i = 1;
		while (i <= nb)
		{
			res = res * i;
			i++;
		}
		return (res);
	}
	else
		return (0);
}
